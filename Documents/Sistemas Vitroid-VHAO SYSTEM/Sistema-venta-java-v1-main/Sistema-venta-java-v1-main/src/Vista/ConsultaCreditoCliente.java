         private void RegistrarVentaCreditos() {
    try {
        // Asegurarse de que el objeto v estÃ© inicializado
        Venta v = new Venta();
        
        // Verificar que txtIdCV no estÃ© vacÃ­o y que contenga un valor numÃ©rico
        String idTexto = txtIdCV.getText();
        if (idTexto.isEmpty()) {
            System.out.println("El ID del cliente no puede estar vacÃ­o.");
            return; // Salir del mÃ©todo si el ID estÃ¡ vacÃ­o
        }
        
        // Convertir el ID a un nÃºmero entero
        int cliente = Integer.parseInt(idTexto);
        
        // Obtener los demÃ¡s datos
        String vendedor = LabelVendedor.getText();
        double monto = TotalPagarCreditos; // AsegÃºrate de que esta variable estÃ© definida y contenga un valor vÃ¡lido

        // Asignar los datos al objeto v
        v.setCliente(cliente);
        v.setVendedor(vendedor);
        v.setTotal(monto);
        
        // Asignar la fecha
        v.setFecha(fechaActual);

        // Registrar la venta
        Vdao.RegistrarVenta(v);
        
    } catch (NumberFormatException e) {
        System.out.println("Error al convertir el ID del cliente: " + e.getMessage());
    } catch (Exception e) {
        System.out.println("Error al registrar la venta: " + e.getMessage());
    }
}
       
       
       
       
   
